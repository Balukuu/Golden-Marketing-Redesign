def load_pool():
    code = WORK_DATA.read_text(encoding="utf-8")
    code = code.replace("const galleryData", "globalThis_x")  # neutralize
    # crude parse: extract the JSON array
    start = code.index("[")
    arr = json.loads(code[start:code.rindex("]") + 1])
    pool = []
    for b in arr:
        for im in b["images"]:
            pool.append({"brand": b["id"], "thumb": im["thumb"],
                         "type": im["type"], "title": im["title"]})
    return pool


def interleave(pool):
    """Round-robin the pool across brands so galleries show a mix, not one brand."""
    by_brand = {}
    for p in pool:
        by_brand.setdefault(p["brand"], []).append(p)
    order, lists = [], list(by_brand.values())
    i = 0
    while any(lists):
        lst = lists[i % len(lists)]
        if lst:
            order.append(lst.pop(0))
        i += 1
        if i > len(pool) * 2:
            break
    return order


def pick(pool, predicate, n, used):
    # filter first, THEN interleave, so a single brand can't monopolise a gallery
    candidates = interleave([p for p in pool if predicate(p) and p["thumb"] not in used])
    out = candidates[:n]
    for p in out:
        used.add(p["thumb"])
    return out


def main():
    for d in (HEROES, SERVICES):
        if d.exists():
            shutil.rmtree(d)

    manifest = {"heroes": {}, "services": {}, "cards": {}}

    # Heroes (full + card thumb)
    for slug, rel in HERO_SRC.items():
        full = save_webp(SRC / rel, HEROES / f"{slug}.webp", 2000, 80)
        thumb = save_webp(SRC / rel, HEROES / "thumbs" / f"{slug}.webp", 900, 78)
        manifest["heroes"][slug] = f"images/heroes/{slug}.webp"
        print(f"hero {slug:32} {full.stat().st_size//1024}KB")
    manifest["cards"] = {  # homepage featured cards -> card-sized client heroes
        k.replace("client-", ""): f"images/heroes/thumbs/{k}.webp"
        for k in HERO_SRC if k.startswith("client-")
    }

    # Service-extra gallery thumbs (Automation / Retail Audit)
    extra = {}
    for slug, rel in EXTRA_SRC.items():
        save_webp(SRC / rel, SERVICES / f"{slug}.webp", 640, 72)
        extra[slug] = f"images/services/{slug}.webp"

    # Service galleries (9 thumbs each). thumbs are relative to site root;
    # service pages live in /services/ so HTML will prefix "../".
    pool = load_pool()

    def merch(p): return p["type"] == "merchandising"
    def act(p): return p["type"] == "activation"

    galleries = {}

    used = set()
    galleries["field-activations"] = [p["thumb"] for p in pick(pool, act, 9, used)]

    used = set()
    galleries["merchandising"] = [p["thumb"] for p in pick(pool, merch, 9, used)]

    used = set()
    road_titles = {"Predator Roadshows", "Rwenzori Marathon", "Rwenzori Marathon Launch",
                   "Fuel On The Go", "Rimula Oils"}
    g = pick(pool, lambda p: p["title"] in road_titles, 9, used)
    g += pick(pool, act, 9 - len(g), used)
    galleries["dynamic-roadshows"] = [p["thumb"] for p in g]

    used = set()
    event_titles = {"World Freestyle Football", "Roast & Rhyme", "Coke Studio",
                    "Shell Expo", "Fanta Movie Time", "Heads Up", "Hamper Promo"}
    g = pick(pool, lambda p: p["title"] in event_titles, 9, used)
    g += pick(pool, act, 9 - len(g), used)
    galleries["events-management"] = [p["thumb"] for p in g]

    used = set()
    g = pick(pool, merch, 6, used) + pick(pool, act, 3, used)
    galleries["distribution-sales"] = [p["thumb"] for p in g]

    used = set()
    # strategy: lead with the office strategy-session photos, then a cross-brand mix
    g = pick(pool, lambda p: p["brand"] == "cocacola", 3, used)
    g += pick(pool, lambda p: p["brand"] == "unilever", 2, used)
    g += pick(pool, lambda p: p["brand"] == "shell", 2, used)
    galleries["strategy"] = [extra["strategy-1"], extra["strategy-2"]] + [p["thumb"] for p in g]

    used = set()
    g = [extra["automation-1"], extra["automation-2"]]
    g += [p["thumb"] for p in pick(pool, act, 7, used)]
    galleries["field-automation-surveys"] = g

    used = set()
    g = [extra["retail-audit-1"], extra["retail-audit-2"]]
    g += [p["thumb"] for p in pick(pool, merch, 7, used)]
    galleries["retail-audit"] = g

    manifest["services"] = galleries

    (ROOT / "scripts" / "assets-manifest.json").write_text(
        json.dumps(manifest, indent=2), encoding="utf-8")
    print("\nService gallery sizes:",
          {k: len(v) for k, v in galleries.items()})
    print("Wrote scripts/assets-manifest.json")


if __name__ == "__main__":
    main()
