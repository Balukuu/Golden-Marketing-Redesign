"""
Build assets for the Services view (8 service pages) and Clients view
(our-work brand heroes + homepage featured cards) from the "GM images" drop.

Outputs:
  images/heroes/<slug>.webp          hero banners (max 2000px, q80)
  images/heroes/thumbs/<slug>.webp   card-sized (max 900px, q78)
  images/services/<slug>.webp        gallery thumbs for Automation/Retail-Audit extras (max 640px)
  scripts/assets-manifest.json       hero + service-gallery + featured-card mapping for HTML wiring

Service galleries mostly REUSE the already-built images/work/<brand>/thumbs/*.webp
(generated by build_work_gallery.py); this script only adds the photos that
don't live in a brand folder (Automation, Retail Audit) plus all hero/card images.
"""
